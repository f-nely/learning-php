<?php

/* 
    Arithmetic Operators
    
    Order of operations (PEMDAS)
    1. Parentheses
    2. Exponents
    3. Multiplication / Division
    4. Assition / Subtraction
*/

$a = 2;
echo $a + 2; // This will output 4
echo '<br>';

echo 5 * 6 + 3 - 1; // This will output 32
echo '<br>';

echo 5 * (6 + 3) - 1; // This will output 44
echo '<br>';

echo 5 ** 2 * (6 + 3) - 1; // This will output 224